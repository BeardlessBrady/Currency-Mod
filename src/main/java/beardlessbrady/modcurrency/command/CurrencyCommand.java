public class CurrencyCommand implements ICommand {
    ArrayList aliases = Lists.newArrayList(ModCurrency.MODID, "CREATE-CURRENCY", "Create-Currency");

    @Override
    public String getName() {
        return "create-currency";
    }

    @Override
    public String getUsage(ICommandSender sender) {
        return "create-currency <currency ID> <texture link>";
    }

    @Override
    public List<String> getAliases() {
        return aliases;
    }

    @Override
    public void execute(MinecraftServer server, ICommandSender sender, String[] args) throws CommandException {
        System.out.println("DDD");
        try {
            File myObj = new File("resourcepacks/filename.txt");
            if (myObj.createNewFile()) {
                System.out.println("File created: " + myObj.getName());
            } else {
                System.out.println("File already exists.");
            }
        } catch (IOException e) {
            System.out.println("An error occurred.");
            e.printStackTrace();
        }
    }

    @Override
    public boolean checkPermission(MinecraftServer server, ICommandSender sender) {
        return true;
    }

    @Override
    public List<String> getTabCompletions(MinecraftServer server, ICommandSender sender, String[] args, @Nullable BlockPos targetPos) {
        return null;
    }

    @Override
    public boolean isUsernameIndex(String[] args, int index) {
        return false;
    }

    @Override
    public int compareTo(ICommand o) {
        return 0;
    }
}
